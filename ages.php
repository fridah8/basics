<?php
$ages=array();
for($x=1;$x<=200;$x++){
$value=rand(1,120);
$ages[]=$value;
}
//var_dump($ages);
$y=0;
do{
$value=$ages[$y];
if($value%2!=0){
echo "$value<br>";
}
$y++;
}while($y<200);
$file=fopen("ages.txt","a+");
$z=0;
while($z<count($ages))
{
$value=$ages[$z];
if($value%2!=0){
fputs($file,"$value\n");
}
$z++;
}
fclose($file);
?>